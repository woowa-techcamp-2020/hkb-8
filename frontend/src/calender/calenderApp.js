import {Header} from '../component/header/header';
import {Body} from '../component/body/body';
import {ListTab, MonthTab} from '../component/body/tap/tab';
import {Calender} from "../component/calender/calender";
import './calenderApp.scss';

const app = document.querySelector('#app'); // 최상단 노드
const headerSection = new Header();
const date = new Date();
const bodySection = new Body(new MonthTab, new ListTab, new Calender());


app.appendChild(headerSection.render());
app.appendChild(bodySection.render());


function renderCalendar() {
    date.setDate(1);

    const monthDays = document.querySelector(".days");

    const lastDay = new Date(
        date.getFullYear(),
        date.getMonth() + 1,
        0
    ).getDate();

    const prevLastDay = new Date(
        date.getFullYear(),
        date.getMonth(),
        0
    ).getDate();

    const firstDayIndex = date.getDay();

    const lastDayIndex = new Date(
        date.getFullYear(),
        date.getMonth() + 1,
        0
    ).getDay();

    const nextDays = 7 - lastDayIndex - 1;

    const months = [
        "1월",
        "2월",
        "3월",
        "4월",
        "5월",
        "6월",
        "7월",
        "8월",
        "9월",
        "10월",
        "11월",
        "12월",
    ];

    // console.log(months[date.getMonth()])
    document.querySelector(".date").innerHTML = date.getFullYear() +'.' + months[date.getMonth()];
    // document.querySelector(".date p").innerHTML = new Date().toDateString();

    let days = '';

    for (let x = firstDayIndex; x > 0; x--) {
        days += `<div class="prev-date">${prevLastDay - x + 1}</div>`;
    }

    for (let i = 1; i <= lastDay; i++) {
        if (
            i === new Date().getDate() &&
            date.getMonth() === new Date().getMonth()
        ) {
            days += `<div class="today">${i}</div>`;
        } else {
            days += `<div>${i}</div>`;
        }
    }

    for (let j = 1; j <= nextDays; j++) {
        days += `<div class="next-date">${j}</div>`;
        monthDays.innerHTML = days;
    }
}

document.querySelector(".prev").addEventListener("click", () => {
    date.setMonth(date.getMonth() - 1);
    renderCalendar();
});

document.querySelector(".next").addEventListener("click", () => {
    date.setMonth(date.getMonth() + 1);
    renderCalendar();
});

renderCalendar();